// Guide to install and setup C#.net and Visual Studio IDE:

// 1. Download and Install Visual Studio:
//    - Go to the official Visual Studio website: https://visualstudio.microsoft.com/
//    - Click on "Download Visual Studio" and choose the edition you want (Community, Professional, or Enterprise).
//    - Run the installer and follow the prompts to install Visual Studio on your computer.

// 2. Install .NET SDK:
//    - During the Visual Studio installation, make sure to select the ".NET desktop development" workload, which includes the .NET SDK.
//    - If you missed it during installation, you can download and install the .NET SDK separately from the official .NET website: https://dotnet.microsoft.com/download

// 3. Create a New C# Project:
//    - Open Visual Studio and click on "Create a new project".
//    - Select "Console App" from the list of project templates and click "Next".
//    - Name your project and choose a location to save it, then click "Create".

// 4. Write Your C# Code:
//    - In the Solution Explorer, open the "Program.cs" file.
//    - Write your C# code inside the Main method.

// 5. Build and Run Your Application:
//    - To build your application, click on "Build" in the menu and select "Build Solution".
//    - To run your application, click on "Debug" in the menu and select "Start Debugging" or press F5. You should see the output of your program in the console window.