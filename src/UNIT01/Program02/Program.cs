// Guide to understand the structure of a C#.net program:
// 1. Namespace: 
//    A namespace is a container for classes and other types. It helps to organize code and prevent naming conflicts. In the example, "Program" is the namespace.

// 2. Class: 
//    A class is a blueprint for creating objects. It contains methods and properties that define the behavior and characteristics of the objects. In the example, "Program" is also the name of the class.

// 3. Main Method:
//    The Main method is the entry point of a C# program. It is where the program starts execution. The Main method must be static and can have a return type of void or int. In the example, the Main method is defined as "static void Main()".

// 4. Statements:
//    Statements are the instructions that the program executes. They are written inside the Main method or other methods. In the example, "Console.WriteLine("Hello, World!");" is a statement that prints "Hello, World!" to the console.

// 5. Comments:
//    Comments are used to explain the code and are ignored by the compiler. They can be single-line comments (starting with //) or multi-line comments (enclosed in /* */). In the example, the comments are used to explain the structure of the program.