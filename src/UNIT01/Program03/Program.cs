// C#.net has a rich set of libraries that provide various functionalities. Some of the commonly used libraries include:
// 1. System: 
//    This is the core library that provides basic functionalities such as input/output, data types, and collections.

// 2. System.Collections.Generic: 
//    This library provides generic collection classes such as List<T>, Dictionary<TKey, TValue>, and Queue<T>.

// 3. System.IO: 
//    This library provides classes for handling input and output operations, such as reading and writing files.

// 4. System.Net: 
//    This library provides classes for working with network protocols, such as HTTP and FTP.

// 5. System.Threading: 
//    This library provides classes for working with threads and asynchronous programming.

// 6. System.Linq: 
//    This library provides classes and methods for querying collections using Language Integrated Query (LINQ).

// 7. System.Text: 
//    This library provides classes for working with text, such as StringBuilder and Encoding.

// 8. System.Xml: 
//    This library provides classes for working with XML data, such as XmlDocument and XmlReader.

// 9. System.Data: 
//    This library provides classes for working with databases, such as DataSet and SqlConnection.

// 10. System.Drawing: 
//    This library provides classes for working with graphics and images, such as Bitmap and Graphics.